public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 252.4478f;
        System.out.printf("%.3f",a);
        System.out.println();
        System.out.printf("%.2f",Math.PI);
        System.out.println();
//        Format Specifiers
        System.out.printf("Hello my name is %s and my family name is %s","Umesh","Nagare");
        System.out.println();
        System.out.printf("The value is %d",27);
    }
}
